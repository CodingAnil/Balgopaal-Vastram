export const getProductsByCategory = (category) => {
  return products.filter((product) => product.category === category)
}

export const getFeaturedProducts = () => {
  return products.filter((product) => product.featured)
}

export const getProductBySlug = (slug) => {
  return products.find((product) => product.slug === slug)
}

export const searchProducts = (query) => {
  const lowercaseQuery = query.toLowerCase()
  return products.filter(
    (product) =>
      product.name.toLowerCase().includes(lowercaseQuery) ||
      product.description.toLowerCase().includes(lowercaseQuery) ||
      product.category.toLowerCase().includes(lowercaseQuery)
  )
}

export const sortProducts = (products, sortBy) => {
  const sortedProducts = [...products]

  switch (sortBy) {
    case 'price-low-high':
      return sortedProducts.sort((a, b) => a.price - b.price)
    case 'price-high-low':
      return sortedProducts.sort((a, b) => b.price - a.price)
    case 'name-a-z':
      return sortedProducts.sort((a, b) => a.name.localeCompare(b.name))
    case 'rating':
      return sortedProducts.sort((a, b) => b.rating - a.rating)
    default:
      return sortedProducts
  }
}

export const filterProducts = (products, filters) => {
  return products.filter((product) => {
    if (filters.category && product.category !== filters.category) {
      return false
    }

    if (filters.color && !product.colors.includes(filters.color)) {
      return false
    }

    if (filters.size && !product.sizes.includes(filters.size)) {
      return false
    }

    if (filters.minPrice && product.price < filters.minPrice) {
      return false
    }

    if (filters.maxPrice && product.price > filters.maxPrice) {
      return false
    }

    return true
  })
}

export const getRelatedProducts = (currentProduct, limit = 4) => {
  // Get products from the same category, excluding the current product
  const relatedProducts = products
    .filter(
      (product) =>
        product.category === currentProduct.category &&
        product.id !== currentProduct.id
    )
    .slice(0, limit)

  // If we don't have enough products from the same category, fill with other products
  if (relatedProducts.length < limit) {
    const additionalProducts = products
      .filter(
        (product) =>
          product.id !== currentProduct.id &&
          !relatedProducts.some((related) => related.id === product.id)
      )
      .slice(0, limit - relatedProducts.length)

    return [...relatedProducts, ...additionalProducts]
  }

  return relatedProducts
}
